#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	src_len;

	if (!dest || !src)
		return (0);
	i = 0;
	src_len = ft_strlen(src);
	while (i + 1 < size && i < src_len)
	{
		dest[i] = src[i];
		i++;
	}
	if (size)
		dest[i] = '\0';
	return (ft_strlen(src));
}
